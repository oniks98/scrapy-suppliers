    availability_lower = str(availability_str).lower().strip()
    
    # –°–ø–æ—á–∞—Ç–∫—É –ø–µ—Ä–µ–≤—ñ—Ä—è—î–º–æ –Ω–∞ –≤—ñ–¥—Å—É—Ç–Ω—ñ—Å—Ç—å (—è–≤–Ω—ñ –Ω–µ–≥–∞—Ç–∏–≤–Ω—ñ –º–∞—Ä–∫–µ—Ä–∏)
