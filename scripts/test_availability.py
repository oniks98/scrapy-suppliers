        "–≤—ñ–¥—Å—É—Ç–Ω—è",
        "–∑–∞–∫—ñ–Ω—á–∏–≤—Å—è",
        "–∑–∞–∫—ñ–Ω—á–∏–ª–∞—Å—å",
        "out of stock",
